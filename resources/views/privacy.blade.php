    <h1>Política de Privacidad - App CEPRE UNAMAD</h1>
    <p>La Universidad Nacional Amazónica de Madre de Dios (UNAMAD), a través de CEPRE UNAMAD, protege la privacidad de sus usuarios.</p>
    
    <h2>1. Uso de la Cámara</h2>
    <p>Nuestra aplicación solicita acceso a la cámara de su dispositivo con el único propósito de permitirle capturar y subir su fotografía para el perfil de usuario y la generación del Carnet Digital. El uso de la cámara es totalmente voluntario.</p>
    
    <h2>2. Protección de Datos</h2>
    <p>Las imágenes capturadas se almacenan de forma segura en nuestros servidores institucionales y no son compartidas con terceros bajo ninguna circunstancia. Estos datos se utilizan exclusivamente para fines de identificación académica dentro de la institución.</p>
    
    <h2>3. Consentimiento</h2>
    <p>Al utilizar la función de cámara en la App, usted otorga su consentimiento para el procesamiento de dicha imagen conforme a los fines académicos descritos.</p>

